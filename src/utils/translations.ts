export function useTranslation(language: string) {
 // Map language codes to supported translations
type Lang = keyof typeof translations;

const getLangCode = (lang: string): Lang => {
  if (lang === 'en-GB') return 'en-GB';
  if (lang.startsWith('en')) return 'en-US';
  if (lang.startsWith('es')) return 'es-ES';
  if (lang.startsWith('fr')) return 'fr-FR';
  if (lang.startsWith('de')) return 'de-DE';
  if (lang.startsWith('pt')) return 'pt-BR';
  if (lang.startsWith('ja')) return 'ja-JP';
  if (lang.startsWith('zh')) return 'zh-CN';
  return 'en-US'; // Default to English
};

const langCode = getLangCode(language);

return (key: keyof typeof translations['en-US']): string => {
  const locale = translations[langCode] as Record<string, string>;
  const fallback = translations['en-US'] as Record<string, string>;
  return locale[key as string] ?? fallback[key as string] ?? (key as string);
};
}