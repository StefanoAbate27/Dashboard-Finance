export type LangCode = keyof typeof additionalTranslations;
export type TranslationKey = keyof typeof additionalTranslations['en-US'];

export function getAdditionalTranslation(
  language: string,
  key: TranslationKey
): string {
  const getLangCode = (lang: string): LangCode => {
    const normalized = (lang || '').toLowerCase();
    if (normalized === 'en-uk' || normalized === 'en-gb' || normalized.startsWith('en-gb')) return 'en-UK';
    if (normalized.startsWith('en')) return 'en-US';
    if (normalized.startsWith('es')) return 'es-ES';
    if (normalized.startsWith('fr')) return 'fr-FR';
    if (normalized.startsWith('de')) return 'de-DE';
    if (normalized.startsWith('pt')) return 'pt-BR';
    if (normalized.startsWith('ja')) return 'ja-JP';
    if (normalized.startsWith('zh')) return 'zh-CN';
    return 'en-US';
  };

  const langCode = getLangCode(language);
  const translationSet = additionalTranslations[langCode] ?? {};
  const fallbackSet = additionalTranslations['en-US'];

  return translationSet[key] ?? fallbackSet[key] ?? key;
}
